/* \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
ГЕНЕРАТОР СЛУЧАЙНОЙ ДАТЫ от И до
СИНТАКСИС:
    getRandomDate(new Date(минимальный год, 0, 1),
                    new Date(максимальный год, 0, 1),
                    генерируем время или нет);
\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\ */
function getRandomDate(min, max = new Date(), time = false) {
    const resultDate = new Date(+min + Math.random() * (max - min));
    if(time){
        return resultDate;
    }
    return new Date(resultDate.getFullYear(), resultDate.getMonth(), resultDate.getDate());
}

/* \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
ПРИНИМАЕТ ПРОИЗВОЛЬНУЮ ДАТУ И ВОЗВРАЩАЕТ СКОЛЬКО ПРОШЛО ЛЕТ С ЭТОЙ ДАТЫ
СИНТАКСИС:
    getYearDate(new Date(начальный год));
\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\ */
function getYearDate(valueDate){
    let currentDate = new Date();
    let resultAge;

    currentDate = new Date(currentDate.getFullYear(), currentDate.getMonth(), currentDate.getDate());
    resultAge = currentDate.getFullYear() - valueDate.getFullYear();

    return new Date(currentDate.getFullYear(), valueDate.getMonth(), valueDate.getDate())
    > currentDate ? --resultAge : resultAge;
}

